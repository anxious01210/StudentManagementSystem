# Overriding the default django Auth User & adding 1 more field in this Model which is user_type ==> user type 1: Admin, 2: Staff, 3: Student by creating a class and passing parent AbstractUser
# so we can extend the default Auth User
class CustomUser(AbstractUser):
    user_type_data = ((1,'HOD'), (2,'Staff'), (3,'Student'))
    user_type = models.CharField(default=1, choices=user_type_data, max_length=10)
# Now all the login of Admin,Staff,Student work with default login of django so I need to relate the main Auth User Model to other Model by admin id , means HOD,Staff,Student will
# relate to admin table by its id ==> admin = models.OneToOneField(CustomUser, on_delete=models.CASCADE)

class AdminHOD(models.Model):
    id = models.AutoField(primary_key=True)
    admin = models.OneToOneField(CustomUser, on_delete=models.CASCADE)
    name = models.CharField(max_length=255)
    email = models.CharField(max_length=255)
    password = models.CharField(max_length=255)
    created_at  = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now_add=True)
    objects = models.Manager()

